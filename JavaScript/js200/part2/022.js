// 022 비교 연산자

/*
    비교 연산자는 두개의 값을 비교하여 true 와 false 결과값을 반환한다. 
    비교 연산자의 종류에는 값이 동등한지 비교하는 일치연산자와 값의 관계를 비교하는 관계연산자가 있다

    일치 연산자는 값의 일치 여부를 확인하며, 종류에는 동등 연산자, 부등 연산자, 일치 연산자, 불일치 연산자가 있다.
*/


console.log(5 == 5);
console.log('5' == 5);
console.log(5 == '5');
console.log(5 != 1);
console.log(5 != '1');
console.log(5 != '5');
console.log(5 === 5);
console.log(5 === '5');
console.log(5 !== 10);
console.log(5 !== '5');

/*
    관계 연산자는 두 개의 값 간의 크기를 비교를 통해 관계를 확인하는 연산자이다.
    관계 연산자에는 < > <= >= 가 있다. 산술식에서 사용되는 비교 연산자와 동일한 의미로 이해하면 된다.
*/

console.log(5 > 3);
console.log(5 < 3);
console.log(5 <= 6);
console.log(5 >= 5);


