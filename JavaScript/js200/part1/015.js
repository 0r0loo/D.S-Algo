/*
    while 반복문은 지시어 while 로 시작한다. 그 다음 소괄호 안에 조건식이 들어가는데, 이 조건식의 결과값은 true 또는 false 만 가능하다
    그리고 조건식이 true 를 만족하는 경우에만 중괄호 {} 안의 문장들이 실행된다. 조건식이 false가 되면 더 이상 반복 실행하지 않는다.
    while 반복문에서도 break와 continue 문을 사용할 수 있다.

    while (조건식) {
        반복하게 될 문장
    }

    do-while 반복문은 맨 앞에 위치한 지시어 do 의 사전적 의미 그대로, 처음은 조건 결과와 상관 없이 무조건 문장을 실행(do) 한다. 그리고
    조건식의 결과값을 확인하고 다음의 흐름은 이전 while 과 동일하다

    do {
        반복하게 될 문장
    } while (조건식)
*/


